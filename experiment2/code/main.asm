MAIN PROC FAR
START:
    MOV DL,10H
    MOV BL,0FH

ROW:
    MOV CX,10H
LINE:
        MOV AL,DL
        OUTPUT AL
        PUSH DX

        OUTPUT 0H
        OUTPUT 0H

        POP DX
        INC DL
    LOOP LINE
    
    PUSH DX

    OUTPUT 0DH
    OUTPUT 0AH

    POP DX
    
    DEC BL
    CMP BL,0H
    JNE ROW
    JE EXIT

